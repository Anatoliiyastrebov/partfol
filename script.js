function getRussianPluralForm(number) {
    const mod10 = number % 10;
    const mod100 = number % 100;
    
    if (mod100 >= 11 && mod100 <= 19) {
        return 'many'; // 11-19 –ª–µ—Ç
    }
    if (mod10 === 1) {
        return 'one'; // 1 –≥–æ–¥
    }
    if (mod10 >= 2 && mod10 <= 4) {
        return 'few'; // 2, 3, 4 –≥–æ–¥–∞
    }
    return 'many'; // 5, 6, 7, 8, 9, 0 –ª–µ—Ç
}

